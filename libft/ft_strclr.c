#include "includes/libft.h"

void		ft_strclr(char *s)
{
	char	*tmp;

	if (s != NULL)
	{
		tmp = s;
		while (*tmp != '\0')
		{
			*tmp = '\0';
			tmp++;
		}
	}
}
